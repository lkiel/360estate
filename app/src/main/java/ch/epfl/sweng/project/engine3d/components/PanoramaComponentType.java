package ch.epfl.sweng.project.engine3d.components;

/**
 * This enum stores all possible components type that can be used in a panorama. This includes but is not limited to
 * objects representing the possibility of having a transition between two panorama as well as objects representing a
 * piece of information that can be displayed to the user
 */
public enum PanoramaComponentType {
    TRANSITION, INFORMATION
}